#for loop example
end_value = int(input("Enter a  number: ")) #5
the_sum = 0

for num in range(end_value + 1): #range(5) -> 0,1,2,3,4, range(5+1) -> 0,1,2,3,4,5
    the_sum += num

print(the_sum)


